CREATE TABLE dog_date_reviews (
    id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    date_created TIMESTAMPTZ NOT NULL,
    dog_id INT REFERENCES dog_date_dog_profiles(id) ON DELETE CASCADE NOT NULL,
    reviewer TEXT REFERENCES dog_date_users(username) ON DELETE CASCADE NOT NULL,
    friendliness_dogs INT NOT NULL,
    friendliness_people INT NOT NULL,
    playing_interest INT NOT NULL,
    obedience INT NOT NULL,
    profile_accuracy INT NOT NULL,
    location_suitability INT NOT NULL,
    address TEXT NOT NULL,
    city TEXT NOT NULL,
    state TEXT NOT NULL,
    zipcode TEXT NOT NULL,
    lat TEXT,
    lon TEXT,
    date TEXT NOT NULL,
    start_time TEXT NOT NULL,
    end_time TEXT NOT NULL,
    personal_message TEXT
);