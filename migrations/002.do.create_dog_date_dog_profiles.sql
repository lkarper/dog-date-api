CREATE TABLE dog_date_dog_profiles (
    id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    owner_id INT REFERENCES dog_date_users(id) ON DELETE CASCADE NOT NULL,
    name TEXT NOT NULL,
    profile_img_url TEXT,
    age_years INT,
    age_months INT,
    sex TEXT NOT NULL,
    breed TEXT,
    weight INT,
    energy TEXT NOT NULL,
    temperment TEXT NOT NULL,
    obedience TEXT NOT NULL,
    dislikes_puppies BOOLEAN NOT NULL,
    dislikes_men BOOLEAN NOT NULL,
    dislikes_women BOOLEAN NOT NULL,
    dislikes_children BOOLEAN NOT NULL,
    recently_adopted BOOLEAN NOT NULL,
    prefers_people BOOLEAN NOT NULL,
    leash_aggression BOOLEAN NOT NULL,
    elderly_dog BOOLEAN NOT NULL,
    little_time_with_other_dogs BOOLEAN NOT NULL,
    much_experience_with_other_dogs BOOLEAN NOT NULL,
    aggressive BOOLEAN NOT NULL,
    owner_description TEXT
);